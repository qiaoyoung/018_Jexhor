Byte * InsideDirectIconDataToCache(Byte *data) {
    int insideAlly = data[0];
    int titleWhich = data[1];
    Byte alienInput = data[2];
    int aspectBlue = data[3];
    if (!insideAlly) return data + aspectBlue;
    for (int i = aspectBlue; i < aspectBlue + titleWhich; i++) {
        int value = data[i] - alienInput;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[aspectBlue + titleWhich] = 0;
    return data + aspectBlue;
}

NSString *StringFromInsideDirectIconData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)InsideDirectIconDataToCache(data)];
