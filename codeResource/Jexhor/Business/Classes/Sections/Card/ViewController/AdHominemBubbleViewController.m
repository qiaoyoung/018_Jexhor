Byte * XerographyDataToCache(Byte *data) {
    int somebody = data[0];
    int slain = data[1];
    Byte practitionerPenalty = data[2];
    int showRandom = data[3];
    if (!somebody) return data + showRandom;
    for (int i = showRandom; i < showRandom + slain; i++) {
        int value = data[i] + practitionerPenalty;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[showRandom + slain] = 0;
    return data + showRandom;
}

NSString *StringFromXerographyData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)XerographyDataToCache(data)];
}
