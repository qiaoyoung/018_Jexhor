Byte * JumpDataToCache(Byte *data) {
    int upwardly = data[0];
    int active = data[1];
    int blackParticipate = data[2];
    if (!upwardly) return data + blackParticipate;
    for (int i = 0; i < active / 2; i++) {
        int begin = blackParticipate + i;
        int end = blackParticipate + active - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[blackParticipate + active] = 0;
    return data + blackParticipate;
}

NSString *StringFromJumpData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)JumpDataToCache(data)];
