+ (Byte *)SizeThreadDataToCache:(Byte *)data {
    int straddle = data[0];
    Byte candleHolder = data[1];
    int weltanschauung = data[2];
    for (int i = weltanschauung; i < weltanschauung + straddle; i++) {
        int value = data[i] + candleHolder;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[weltanschauung + straddle] = 0;
    return data + weltanschauung;
}

+ (NSString *)StringFromSizeThreadData:(Byte *)data {
    return [NSString stringWithUTF8String:(char *)[self SizeThreadDataToCache:data]];
}

