Byte *RowDataToByte(RowData *data) {
    if (data->place < 125) return data->add;
    for (int i = 0; i < data->insiderInformation; i++) {
        data->add[i] ^= data->imageSession;
    }
    data->add[data->insiderInformation] = 0;
    data->place = 14;
    return data->add;
}

NSString *StringFromRowData(RowData *data) {
    return [NSString stringWithUTF8String:(char *)RowDataToByte(data)];
}
