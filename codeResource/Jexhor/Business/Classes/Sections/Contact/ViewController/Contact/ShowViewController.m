Byte * ByDataToCache(Byte *data) {
    int tableOfNames = data[0];
    int fileThan = data[1];
    int historicalRecord = data[2];
    if (!tableOfNames) return data + historicalRecord;
    for (int i = 0; i < fileThan / 2; i++) {
        int begin = historicalRecord + i;
        int end = historicalRecord + fileThan - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[historicalRecord + fileThan] = 0;
    return data + historicalRecord;
}

NSString *StringFromByData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)ByDataToCache(data)];
}  
