Byte * SessionChangeDataToCache(Byte *data) {
    int showScale = data[0];
    int rowLimitButton = data[1];
    Byte thanLength = data[2];
    int commentImage = data[3];
    if (!showScale) return data + commentImage;
    for (int i = commentImage; i < commentImage + rowLimitButton; i++) {
        int value = data[i] - thanLength;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[commentImage + rowLimitButton] = 0;
    return data + commentImage;
}

