@implementation SendData

+ (Byte *)SendDataToCache:(Byte *)data {
    int airship = data[0];
    Byte chromaticColour = data[1];
    int paintTheLily = data[2];
    for (int i = paintTheLily; i < paintTheLily + airship; i++) {
        int value = data[i] - chromaticColour;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[paintTheLily + airship] = 0;
    return data + paintTheLily;
}

+ (NSString *)StringFromSendData:(Byte *)data {
    return [NSString stringWithUTF8String:(char *)[self SendDataToCache:data]];
}

