- (Byte *)InspireMothDataToCache:(Byte *)data {
    int larva = data[0];
    Byte toss = data[1];
    int want = data[2];
    for (int i = want; i < want + larva; i++) {
        int value = data[i] + toss;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[want + larva] = 0;
    return data + want;
}

- (NSString *)StringFromInspireMothData:(Byte *)data {
    return [NSString stringWithUTF8String:(char *)[self InspireMothDataToCache:data]];
}

