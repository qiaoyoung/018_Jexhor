Byte * ExpenseDataToCache(Byte *data) {
    int presentEnormous = data[0];
    int quetchThree = data[1];
    int colorIts = data[2];
    if (!presentEnormous) return data + colorIts;
    for (int i = 0; i < quetchThree / 2; i++) {
        int begin = colorIts + i;
        int end = colorIts + quetchThree - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[colorIts + quetchThree] = 0;
    return data + colorIts;
}

NSString *StringFromExpenseData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)ExpenseDataToCache(data)];
}  
