Byte * AmbitionDataToCache(Byte *data) {
    int showerheadAnyway = data[0];
    int grateQuick = data[1];
    int textFederal = data[2];
    if (!showerheadAnyway) return data + textFederal;
    for (int i = 0; i < grateQuick / 2; i++) {
        int begin = textFederal + i;
        int end = textFederal + grateQuick - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[textFederal + grateQuick] = 0;
    return data + textFederal;
}

NSString *StringFromAmbitionData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)AmbitionDataToCache(data)];
}  
